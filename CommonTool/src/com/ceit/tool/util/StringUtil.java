package com.ceit.tool.util;

public class StringUtil {
	public static String toBinstr(String args) {
		String binstr = "";
		for(int i=0;i<args.length();i++) {
			binstr += Integer.toBinaryString(args.charAt(i));
		}
		return binstr;
	}
	
	public static String showString(Object o){
		return o==null?"":o.toString();
	}
}
